def get_stats(results, alg=1):
    # collect information on labeling accuracy across 100 different cutoff values
    checker = {i[0]: i[1] for i in truth}

    FPs, TPs, TNs, FNs = [], [], [], []
    corrects, incorrects = [], []

    for num in range(101):
        cutoff = .01 * num

        FP, FN, TP, TN = 0, 0, 0, 0

        for score, entry in results:
            mylabel = label(score, cutoff=cutoff) if alg in [1, 4] else label(score, cutoff=cutoff, cutoff_is_min=False)
            actual = checker[entry.alg1]
            if mylabel == actual:
                if actual: TP += 1
                else: TN += 1
            else:
                if actual: FN += 1
                else: FP += 1

        FPs.append(FP/(FP + TN))
        TPs.append(TP/(TP + FN))
        TNs.append(TN/(FP + TN))
        FNs.append(FN/(TP + FN))
        total = sum([FP, FN, TP, TN])
        corrects.append((TP + TN) / total)
        incorrects.append((FP + FN) / total)

    return TPs, TNs, FPs, FNs, corrects, incorrects


def plot_roc(algs=[]):
    # display roc curves for each algorithm in algs
    labels = {1:'Letter-Pair Frequency',
              2:'Upr/Lwr Transitions',
              3:'Alph/Num Transitions',
              4:'Random'}
    for alg in algs:
        results = get_results(alg)
        TPs, _, FPs, _, _, _ = get_stats(results, alg)
        plt.plot(FPs, TPs, label=labels[alg])

    plt.title('ROC Comparison')
    plt.xlabel('FPR')
    plt.ylabel('TPR')
    plt.legend()
    plt.show()


def plot_rates(alg=1, rates=['TP', 'FP', 'TN', 'FN']):
    # display selected accuracy metrics individually
    results = get_results(alg)
    TPs, TNs, FPs, FNs, _, _ = get_stats(results, alg)
    if 'FP' in rates: plt.plot([.01 * i for i in range(len(FPs))], FPs, 'r', label='FPR')
    if 'TP' in rates: plt.plot([.01 * i for i in range(len(TPs))], TPs, 'g', label='TPR')
    if 'TN' in rates: plt.plot([.01 * i for i in range(len(TNs))], TNs, 'b', label='TNR')
    if 'FN' in rates: plt.plot([.01 * i for i in range(len(FNs))], FNs, 'orange', label='FNR')

    plt.title(f'Rate Summary: Algorithm {alg}')
    plt.xlabel('Cutoff Value')
    plt.ylabel('Metric Value')
    plt.legend()
    plt.show()


def plot_correct_incorrect_percentages(alg=1):
    # display the percentage of correct and incorrect labels
    results = get_results(alg)
    _, _, _, _, corrects, incorrects = get_stats(results, alg)
    plt.plot([.01*i for i in range(len(corrects))], corrects, color='g', label='Correct')
    plt.plot([.01*i for i in range(len(incorrects))], incorrects, color='r', label='Incorrect')
    plt.title('% Correct vs. % Incorrect')
    plt.xlabel('Cutoff Value')
    plt.ylabel('Percentage of Data Set')
    plt.legend()
    plt.show()


def plot_correct_incorrect_percentage_deltas(alg=1):
    # display the percentage change in correct and incorrect labels
    results = get_results(alg)
    _, _, _, _, corrects, incorrects = get_stats(results, alg)
    correct_delta = [corrects[0]]
    correct_delta.extend([corrects[i] - corrects[i-1] for i in range(1, len(corrects))])
    incorrect_delta = [incorrects[0]]
    incorrect_delta.extend([incorrects[i] - incorrects[i - 1] for i in range(1, len(incorrects))])
    plt.plot([.01 * i for i in range(len(correct_delta))], correct_delta, color='g', label='%$\Delta$ Correct')
    plt.plot([.01 * i for i in range(len(incorrect_delta))], incorrect_delta, color='r', label='%$\Delta$ Incorrect')
    plt.xlabel('Cutoff Value')
    plt.ylabel('Change in Accuracy from Previous Cutoff')
    plt.legend()
    plt.show()


