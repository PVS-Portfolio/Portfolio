class Entry:
    # a basic class to represent a single data point
    # each string has to be preprocessed differently for the different algorithms

    def __init__(self, website, stripped, alg1, alg2, alg3):
        self.website = website
        self.stripped = stripped
        self.alg1 = alg1
        self.alg2 = alg2
        self.alg3 = alg3


def get_raw_inputs():
    # grab and clean entries from database.csv (not included)
    f = open('database.csv', 'r')
    rawdata = f.readlines()[1:]
    f.close()

    data = [i[:-1].split(',') for i in rawdata]
    websites = [i[2] for i in data]
    links = {}
    inputs = []
    for w in websites:
        mylist = w.split('#')
        for website in mylist:
            ret = []
            if not website:
                continue
            if ' ' in website:
                continue

            clean = website
            for item in ['http://', 'https://', 'www.', '/']:
                clean = clean.replace(item, '')
            for item in ['.com', '.org', '.net', '.llc', '.inc']:
                clean = clean[:clean.index(item)]
            links[clean] = website

            ret.extend([website, clean, clean.lower()])
            s = ''
            for char in clean:
                if char in lowercase + uppercase:
                    s += char
            ret.append(s)
            s = ''
            for char in clean.lower():
                if char in lowercase + numbers:
                    s += char
            ret.append(s)
            entry = Entry(ret[0], ret[1], ret[2], ret[3], ret[4])
            inputs.append(entry)

    return inputs


def get_distribution():
    # get letter-pair frequency distribution
    counts = {}
    for word in words:
        if len(word) == 1:                          # skip one-letter words
            continue
        for i in range(len(word) - 1):              # add to running total for each letter-pair
            chunk = word[i:i+2]
            if chunk in counts:
                counts[chunk] += 1
            else:
                counts[chunk] = 1

    maxcount = max(counts.values())                 # normalize frequency counts
    percents = {chunk: counts[chunk] / maxcount for chunk in counts}
    return percents


def label(s, cutoff=0.11, cutoff_is_min=True):
    # label a string based on its score
    # if cutoff_is_min is True, then scores <= cutoff are False
    # if cutoff_is_min is Fales, then scores >= cutoff are False
    if cutoff_is_min:
        if s <= cutoff:
            return False
        else:
            return True
    else:
        if s >= cutoff:
            return False
        else:
            return True


def score_alg1(s):
    # scores based on frequency of letter pairs
    letters = 'abcdefghijklmnopqrstuvwxyz'
    dist = get_distribution()
    mynum = 0
    for i in range(len(s) - 1):
        chunk = s[i:i+2]                        # get current letter pair
        if chunk in dist:
            mynum += dist[chunk]                # accumulate score if pair is represented

    # count number of valid characters in string
    chars = sum([1 if i in letters else 0 for i in s])
    if chars > 0:
        results = mynum / chars
    else:
        results = 0.
    return results


def score_alg2(s):
    # BAD ALGORITHM 1
    # counts transitions between upper and lowercase
    mynum = 0
    count = 0
    up = s[0] in uppercase
    for char in s:
        if char in lowercase:
            if up:                              # current char is lower, previous was upper
                up = False
                mynum += 1
                count += 1
            else:                               # current char is lower, previous was lower
                count += 1
        elif char in uppercase:
            if up:                              # current char is upper, previous was upper
                count += 1
            else:                               # current char is upper, previous was lower
                up = True
                mynum += 1
                count += 1
        else:
            continue
    if count > 0:
        results = mynum / count
    else:
        results = 0.                            # don't divide by 0 if no score accumulated
    return results


def score_alg3(s):
    # BAD ALGORITHM 2
    # checks transitions between letters and numbers
    mynum = 0
    count = 0
    alph = s[0] in lowercase + uppercase
    for char in s:
        if char in numbers:
            if alph:                            # current char is number, previous was alpha
                alph = False
                mynum += 1
                count += 1
            else:                               # current char is number, previous was number
                count += 1
        elif char in uppercase + lowercase:
            if alph:                            # current char is alpha, previous was alpha
                count += 1
            else:                               # current char is alpha, previous was number
                alph = True
                mynum += 1
                count += 1
        else:                                   # skip other characters
            continue
    if count > 0:
        results = mynum / count
    else:
        results = 0.                            # don't divide by 0 if no score accumulated
    return results


def get_results(alg):
    # make list of Entry objects and run the appropriate scoring algorithm
    results = [Entry(a, b, c, d, e) for a, b, c, d, e in all_inputs]
    if alg == 1:
        results = [(score_alg1(s.alg1), s) for s in results]
    if alg == 2:
        results = [(score_alg2(s.alg2), s) for s in results]
    if alg == 3:
        results = [(score_alg3(s.alg3), s) for s in results]
    if alg == 4:
        results = [(random.random(), s) for s in results]
    results.sort(key=lambda x: x[1].stripped)
    return results


